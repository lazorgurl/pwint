pub fn fowmat(s: &str) -> String {
        s.replace("ike", "iek")
        .replace("why", "y")
        .replace("ough", "o")
        .replace(" w", " w-w")
        .replace("you", "u")
        .replace("nk", "ncc")
        .replace("th", "d")
        .replace('l', "w")
        .replace('r', "w")
        + " uwu"

        
}
